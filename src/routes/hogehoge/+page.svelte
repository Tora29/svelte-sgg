<h1>Svelte Kit SSG では画面遷移が発生した場合、更新差分のDOM のみ更新します。</h1>
<h3>
	F12を押下して開発者ツールを表示し、「Elements（要素）」タブに移動してください。<br />
	リンクを押下した際に&lt;nav> タグがハイライトされないことを確認してください。<br />
	DOMが更新されると、その部分が自動的にハイライトされるので、画面遷移時に更新がない場合はDOM 更新が行われないことがわかります。<br />
  これにより、実際はMPA だが、ユーザー目線ではSPA の挙動となります。パスは変われどページのリロードは発生しないわけです。
</h3>
